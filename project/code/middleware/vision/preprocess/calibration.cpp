void image_calibration(uint8_t* dest_image_data, const uint8_t* source_image_data)
{
  for (int y = 0; y < calibrated_height; y++)
  {
    for (int x = 0;x < calibrated_width;x++)
    {
      int16_t perspective_idx = perspective_mapping[y * calibrated_width + x];
      if (perspective_idx == -1)
      {
        dest_image_data[y * calibrated_width + x] = 0;
      }
      else
      {
        dest_image_data[y * calibrated_width + x] = source_image_data[perspective_idx];
      }
    }
  }

}
